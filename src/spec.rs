pub fn generate_spec(bundle: &str, rootless: bool){

	let config_file = Path::new("config.json");
	let display = config_file.display();
	let mut spec_obj: runtime::Spec;
	
	// Change base dir if --bundle or -b is specified
	if(!bundle.is_empty()){
		let ret = match unistd::chdir(Path::new(bundle)) {
			Err(err) => panic!("Error: while changing to bundle path {}", err),
			Ok(success) => success,
		};
	}

	// Generate rootless spec if --rootless is set
	if(rootless){
		spec_obj = serde_json::from_str(&BASE_SPEC_ROOTLESS).unwrap();
	}else{
		spec_obj = serde_json::from_str(&BASE_SPEC).unwrap();
	}


	match runtime::Spec::save(&spec_obj, "config.json") {
		Ok(_) => {},
		Err(e) => panic!("Error: Unable to write spec {}", e)
	}
	
}

